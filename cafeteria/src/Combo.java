

public class Combo {
	
	private int valorPromocional;
	private String promoção;
	private String nome; 
	
	
	public int getValorPromocional() {
		return valorPromocional;
	}
	public void setValorPromocional(int valorPromocional) {
		this.valorPromocional = valorPromocional;
	}
	public String getPromoção() {
		return promoção;
	}
	public void setPromoção(String promoção) {
		this.promoção = promoção;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	

}
